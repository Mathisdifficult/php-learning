//RegularExpression正则表达式
function Email(){
    $string = 'admin@0509.uu.me';
    if (preg_match("/^[a-zA-Z0-9_-]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+$/",$string)){
        echo "邮箱验证通过！<br>";
    }
    else {
        echo "邮箱格式不符合要求！<br>";
    }
}
Email();

function MobilePhoneAndTelePhone(){
    $string = '0571-8711612';//4+7位或者4+8位或者3+8位^\d{3}-\d{8}$|^\d{4}-\d{7,8}$
    $string1 = "+8613251001010";//86或者+86开头的国家编码^((\+86)|(86))?(13)\d{9}$
    $string2 = '13251001010';//11位 1开头的手机号 ^1[0-9]{10}$
    if (preg_match("/^\d{3}-\d{8}$|^\d{4}-\d{7,8}$|^((\+86)|(86))?(13)\d{9}$|^1[0-9]{10}$/",$string)){
        echo "号码验证通过！<br>";
    }
    else {
        echo "号码格式不符合要求！<br>";
    }
}
MobilePhoneAndTelePhone();

function PasswordExam(){
    $string = 'A234516';
    if (preg_match("/^[a-zA-Z][0-9a-zA-Z!@#$^]{6,18}$/",$string)){
        echo "密码验证通过！<br>";
    }
    else {
        echo "密码格式不符合要求！<br>";
    }
}
PasswordExam();

function ColorOfHexadecimal(){
    $string = '#4f3a45';
    if (preg_match("/^#[0-9a-fA-F]{3,6}$/",$string)){
        echo "十六进制颜色格式验证通过！<br>";
    }
    else {
        echo "十六进制颜色格式不符合要求！<br>";
    }
}
ColorOfHexadecimal();

